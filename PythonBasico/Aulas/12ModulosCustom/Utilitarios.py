def soma(a, b, c):
    somatorio = a+b+c
    return somatorio


def mult(a, b, c):
    produto = a+b+c
    return produto


def isPalindromo(string):
    if string == string[::-1]:  # Se String for igual o contrario
        return True
    else:
        return False


def divisao(a, b):
    return a/b
